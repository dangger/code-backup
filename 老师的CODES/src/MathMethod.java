
public class MathMethod {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		short sh=-25;
		System.out.println(Math.abs(sh));//abs求绝对值
		int a=(int)Math.cbrt(27);//求立方根
		System.out.println(a);
		double d=Math.pow(2, 3);//求2的3次方
		double r=Math.random();//random()方法可以返回一个double类型的随机值，0.0<=该随机值<1.0
		double b=Math.sqrt(16);//求平方根
	}
}
