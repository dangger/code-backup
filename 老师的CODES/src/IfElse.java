
public class IfElse {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int score=50;
		if(score>=60){
			System.out.println("恭喜！");
			System.out.println("成绩及格了！");
		}else{
			System.out.println("抱歉！");
			System.out.println("成绩不及格！");
		}
		
		/*
		 * 双分支
		 * if(条件必须是布尔表达式){
		 * 		//条件满足时要执行的语句，可以是一句话，也可以是多句话
		 * }else{
		 * 		//条件不满足时要执行的语句，可以是一句话，也可以是多句话
		 * }
		 */
	}

}
