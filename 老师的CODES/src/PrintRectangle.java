public class PrintRectangle {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		for (int i = 1; i <= 3; i++) {
			for (int j = 1; j <= 4; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println("------------------------");

		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println("------------------------");
		
		for (int i = 1; i <= 8; i++) {
			for (int j = 1; j <= 10; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println("------------------------");

	}

}
